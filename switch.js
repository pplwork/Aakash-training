const shop = (grade) =>{
  switch(grade){
    case 'A':
      console.log('I can travel')
      break;
    case 'B':
      console.log('I can go to shopping')
      break;
    case 'C':
      console.log('I can watch TV')
      break;
    case 'D':
      console.log('I can go to library')
      break;
  }
}

shop('C');